void RuntimeFault( const char* szText )
{
    lcd_printf("\014ERROR:\r\n%s\r\n", szText );
    debug_printf( "ERROR: %s\r\n", szText );

    HARD_BREAKPOINT();
}

void *operator new( size_t n )
{
    return private_malloc( n );
}

void *operator new[]( size_t n )
{
    return private_malloc( n );
}

void operator delete( void* p )
{
    return private_free( p );
}

void operator delete[]( void* p )
{
    return private_free( p );
}

